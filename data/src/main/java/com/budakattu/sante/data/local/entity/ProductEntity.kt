package com.budakattu.sante.data.local.entity

import androidx.room.Entity
import androidx.room.PrimaryKey
import com.budakattu.sante.domain.model.Product
import com.budakattu.sante.domain.model.ProductAvailability

@Entity(tableName = "products")
data class ProductEntity(
    @PrimaryKey
    val productId: String,
    val familyId: String,
    val categoryId: String,
    val name: String,
    val description: String,
    val audioDescription: String,
    val categoryName: String,
    val familyName: String,
    val village: String,
    val pricePerUnit: Float,
    val mspPerUnit: Float,
    val unit: String,
    val availableStock: Int,
    val reservedStock: Int,
    val soldStock: Int,
    val preorderLimit: Int,
    val isSeasonal: Boolean,
    val season: String?,
    val imageUrls: List<String>,
    val availability: ProductAvailability,
    val isPrebookEnabled: Boolean,
    val expectedDispatchDate: String?,
    val isAvailable: Boolean,
    val isDraft: Boolean,
    val addedAt: Long,
    val lastModifiedAt: Long,
)

fun ProductEntity.asExternalModel() = Product(
    productId = productId,
    familyId = familyId,
    categoryId = categoryId,
    name = name,
    description = description,
    audioDescription = audioDescription,
    categoryName = categoryName,
    familyName = familyName,
    village = village,
    pricePerUnit = pricePerUnit,
    mspPerUnit = mspPerUnit,
    unit = unit,
    availableStock = availableStock,
    reservedStock = reservedStock,
    soldStock = soldStock,
    preorderLimit = preorderLimit,
    isSeasonal = isSeasonal,
    season = season,
    imageUrls = imageUrls,
    availability = availability,
    isPrebookEnabled = isPrebookEnabled,
    expectedDispatchDate = expectedDispatchDate,
    isAvailable = isAvailable,
    isDraft = isDraft,
    addedAt = addedAt,
    lastModifiedAt = lastModifiedAt,
)

fun Product.asEntity() = ProductEntity(
    productId = productId,
    familyId = familyId,
    categoryId = categoryId,
    name = name,
    description = description,
    audioDescription = audioDescription,
    categoryName = categoryName,
    familyName = familyName,
    village = village,
    pricePerUnit = pricePerUnit,
    mspPerUnit = mspPerUnit,
    unit = unit,
    availableStock = availableStock,
    reservedStock = reservedStock,
    soldStock = soldStock,
    preorderLimit = preorderLimit,
    isSeasonal = isSeasonal,
    season = season,
    imageUrls = imageUrls,
    availability = availability,
    isPrebookEnabled = isPrebookEnabled,
    expectedDispatchDate = expectedDispatchDate,
    isAvailable = isAvailable,
    isDraft = isDraft,
    addedAt = addedAt,
    lastModifiedAt = lastModifiedAt,
)
